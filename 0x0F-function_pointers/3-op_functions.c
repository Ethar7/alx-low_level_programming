#include "3-calc.h"
/**
 * op_add - adds int
 * @a: first int
 * @b: second int
 * Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two int
 * @a: first int
 * @b: second int
 *
 * Return: the difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two int
 * @a: first int
 * @b: second int
 * 
 * Return: multiply
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two int
 * @a: first int
 * @b: second int
 *
 * Return: divide
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - divide two int
 * @a: first int
 * @b: second int
 *
 * Return: mod
*/
int op_mod(int a, int b)
{
	return (a % b);
}
