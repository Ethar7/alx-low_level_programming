#include "3-calc.h"

/**
 * op_add - adds two intger
 * @a: int
 * @b: int
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two int
 * @a: int
 * @b: int
 *
 * Return: subtract
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two num
 * @a: int
 * @b: int
 *
 * Return: mult
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide 2 int
 * @a: int
 * @b: int
 *
 * Return: a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modelus
 * @a: int
 * @b: int
 * Return: remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
